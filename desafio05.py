n1 = int(input('digite o numero desejado: '))                                                            #Recebe um numero e converte de string para inteiro.
ant = n1 - 1                                                                                             #Cria uma variavel que calcula e guarda o antecessor de um numero digitado.
suc = n1 + 1                                                                                             #Cria uma variavel que calcula e guarda o sucessor de um numero digitado.
print ('o numero digitado é {}, seu antecessor é {} e seu sucessor é {}' .format(n1, ant, suc))          #Imprime o numero digitado, seu antecessor e seu sucessor.
       
     
